package model;

/**
 *
 * @author APRENDIZ
 */
public class Aprendiz {
    int id;
    int Docu;
    String Nombrea;
    String Apellidoa;
    String Correoa;
    int Numcontacto;

    public Aprendiz() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getDocu() {
        return Docu;
    }

    public void setDocu(int Docu) {
        this.Docu = Docu;
    }

    public String getNombrea() {
        return Nombrea;
    }

    public void setNombrea(String Nombrea) {
        this.Nombrea = Nombrea;
    }

    public String getApellidoa() {
        return Apellidoa;
    }

    public void setApellidoa(String Apellidoa) {
        this.Apellidoa = Apellidoa;
    }

    public String getCorreoa() {
        return Correoa;
    }

    public void setCorreoa(String Correoa) {
        this.Correoa = Correoa;
    }

    public int getNumcontacto() {
        return Numcontacto;
    }

    public void setNumcontacto(int Numcontacto) {
        this.Numcontacto = Numcontacto;
    }
    
}
